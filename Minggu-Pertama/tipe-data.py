# Hari 1-2: Variabel, Tipe Data (string, integer, float, boolean)

# Tipe data adalah jenis data yang digunakan untuk menyimpan nilai dalam variabel.
# Dalam Python, ada beberapa tipe data dasar yang sering digunakan, antara lain:
# 1. String: Tipe data untuk menyimpan teks.
# 2. Integer: Tipe data untuk menyimpan bilangan bulat.
# 3. Float: Tipe data untuk menyimpan bilangan pecahan (desimal).
# 4. Boolean: Tipe data untuk menyimpan nilai True atau False.
# 5. List: Tipe data untuk menyimpan sekumpulan nilai dalam satu variabel.
# 6. Tuple: Tipe data mirip dengan list, tetapi tidak dapat diubah (immutable).
# 7. Dictionary: Tipe data untuk menyimpan pasangan kunci-nilai.
# 8. Set: Tipe data untuk menyimpan sekumpulan nilai unik.

# Mari kita lihat contoh penggunaan masing-masing tipe data tersebut.
# String
nama = "John Doe"  # Tipe data string
print("Nama:", nama)  # Output: Nama: John Doe

# Integer
umur = 25  # Tipe data integer
print("Umur:", umur)  # Output: Umur: 25

# Float
berat = 70.5  # Tipe data float
print("Berat:", berat)  # Output: Berat: 70.5

# Boolean
is_student = True  # Tipe data boolean
print("Apakah mahasiswa?", is_student)  # Output: Apakah mahasiswa? True

# List
buah = ["apel", "jeruk", "pisang"]  # Tipe data list
print("Buah:", buah)  # Output: Buah: ['apel', 'jeruk', 'pisang']

# Mengakses elemen list
print("Buah pertama:", buah[0])  # Output: Buah pertama: apel

# Mengubah elemen list
buah[1] = "mangga"  # Mengubah elemen kedua
print("Buah setelah diubah:", buah)  # Output: Buah setelah diubah: ['apel', 'mangga', 'pisang']
